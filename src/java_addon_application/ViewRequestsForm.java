    private void jButtonAddRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddRequestActionPerformed
        // TODO add your handling code here:
        
        try{
               
               int service_user_id = Integer.valueOf(jTextFieldServiceUserID.getText());
               int test_number = Integer.valueOf(jTextFieldTestNumber.getText());
               

               
               if(request.addRequests(service_user_id, test_number))
               {
                   JOptionPane.showMessageDialog(rootPane, "New Request Added Successfully", "Add request", JOptionPane.INFORMATION_MESSAGE);
               }
               else
               {
                   JOptionPane.showMessageDialog(rootPane, "Request NOT Added", "Add request Error", JOptionPane.ERROR_MESSAGE);
               }
               
           } 
           catch(NumberFormatException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + " - Enter The Test number + Service User ID", "Input Fields Error", JOptionPane.ERROR_MESSAGE);
            }
                           
                             
               
           
    }//GEN-LAST:event_jButtonAddRequestActionPerformed

    private void jButtonEditRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditRequestActionPerformed
       
               
            try{
                int requestID = Integer.valueOf(jTextFieldRequestID.getText());
                int testNumber = Integer.valueOf(jTextFieldTestNumber.getText());
                int serviceuserID = Integer.valueOf(jTextFieldServiceUserID.getText());

                if(request.editRequests(requestID, testNumber, serviceuserID))
                 {
                      JOptionPane.showMessageDialog(rootPane, "Request Information  Updated Successfully", "Edit Request", JOptionPane.INFORMATION_MESSAGE);
                 }
            
             else{
            
                 JOptionPane.showMessageDialog(rootPane, "Request Information Not Updated", "Data Error", JOptionPane.ERROR_MESSAGE);
                }
                
            }
            catch(NumberFormatException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + " - Enter The Test number + The Service User ID + Request ID", "Test Number Error", JOptionPane.ERROR_MESSAGE);
            }
                       
        
        
    }//GEN-LAST:event_jButtonEditRequestActionPerformed

    private void jButtonRemoveRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveRequestActionPerformed
        // TODO add your handling code here:
        
         // delete the selected room
        try{
                int requestId = Integer.valueOf(jTextFieldRequestID.getText());
                
                if(request.removeRequests(requestId))
                 {
                      JOptionPane.showMessageDialog(rootPane, "Request Deleted Successfully", "Remove Request", JOptionPane.INFORMATION_MESSAGE);
                 }
            
             else{
            
                 JOptionPane.showMessageDialog(rootPane, "Request Not Deleted", "Remove Request Error", JOptionPane.ERROR_MESSAGE);
                }
                
            }
            catch(NumberFormatException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + " - Enter The Request ID", "Request ID Error", JOptionPane.ERROR_MESSAGE);
            }
        
    }//GEN-LAST:event_jButtonRemoveRequestActionPerformed

