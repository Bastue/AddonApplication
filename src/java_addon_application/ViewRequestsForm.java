    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       
            // get the jtabel model
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            
            // get the selected row index
            int rIndex = jTable1.getSelectedRow();
            
            // display data
            jTextFieldRequestID.setText(model.getValueAt(rIndex, 0).toString());
            jTextFieldServiceUserID.setText(model.getValueAt(rIndex, 1).toString());
            jTextFieldTestNumber.setText(model.getValueAt(rIndex, 2).toString());
            
        
    }//GEN-LAST:event_jTable1MouseClicked

